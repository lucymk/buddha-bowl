const random = (obj) => {
  var keys = Object.keys(obj);
  const ingredient = keys[(keys.length * Math.random()) << 0];
  const info = obj[ingredient];

  return { ingredient, info };
};

const random2 = (obj) => {
  var keys = Object.keys(obj);
  const ingredient = keys[(keys.length * Math.random()) << 0];
  let ingredient2 = keys[(keys.length * Math.random()) << 0];

  while (ingredient == ingredient2) {
    ingredient2 = keys[(keys.length * Math.random()) << 0];
  }

  const info = obj[ingredient];
  const info2 = obj[ingredient2];

  const both = [
    { ingredient, info },
    { ingredient: ingredient2, info: info2 },
  ];

  const both2 = {
    ingredient: `${ingredient} and ${ingredient2}`,
    info: info,
    info2: info2,
  };

  return both2;
};

